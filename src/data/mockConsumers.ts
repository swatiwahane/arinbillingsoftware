export const getConsumerHistory = (consumerNo: string): Consumer[] => {
  return mockConsumers
    .filter((c) => c.consumerNo === consumerNo)
    .sort((a, b) => {
      const dateA = new Date(a.readingDate);
      const dateB = new Date(b.readingDate);
      return dateB.getTime() - dateA.getTime();
    });
};
